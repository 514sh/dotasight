export const heroByName = Object.fromEntries(
  HEROES.map((h) => [h.name.toLowerCase(), h]),
);
export const heroById = Object.fromEntries(HEROES.map((h) => [h.id, h]));

export const resolveHero = (rec) => {
  if (rec.hero_id && heroById[rec.hero_id]) return heroById[rec.hero_id];
  if (rec.hero_name) return heroByName[rec.hero_name.toLowerCase()];
  return null;
};

